public class Item {

    private String name;
    private Float price;

    //setters
    public void setName(String name){
        this.name = name;
    }

    public void setPrice(Float price){
        this.price = price;
    }

    //getters

    public String getName(){
        return this.name;
    }

    public Float getPrice(){
        return this.price;
    }
}

